
public class Uzaklik {
   private String sehir;
   private String km;

    public Uzaklik(String sehir, String km) {
        this.sehir = sehir;
        this.km = km;
    }

    public String getSehir() {
        return sehir;
    }

    public void setSehir(String sehir) {
        this.sehir = sehir;
    }

    public String getKm() {
        return km;
    }

    public void setKm(String km) {
        this.km = km;
    }
   
    
}
